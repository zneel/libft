int	ft_pow(int nb, int pow)
{
	int res;

	res = 1;
	if (pow < 0)
		return (0);
	if (pow == 0)
		return (1);
	while (pow > 0)
	{
		res *= nb;
		--pow;
	}
	return (res);
}
